USE PUBS
Print LEFT ('abcdefg',4)  			--return 		abcd
Print RIGHT('abcde',3)				--return 		cde
Print LOWER('MiXed CasE')			--Return		mixed case
Print UPPER('MiXed CasE')			--Return		MIXED CASE
Print RTRIM('SQL      ')			--return		'SQL'
Print LTRIM('       SQL')			--return		'SQL'	
Print LEN('abcdef')			      	--return		6
Print REPLACE('abcdefg' ,'abc' ,'123')     	--Return   		123defg
Print Substring('ABCDEFG', 4, 2)             	--Return		DE
